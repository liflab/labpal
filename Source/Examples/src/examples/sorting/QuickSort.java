package examples.sorting;

import ca.uqac.lif.labpal.region.Point;

/**
 * Quick sort algorithm, as found here:
 * http://www.java2novice.com/java-sorting-algorithms/quick-sort/
 */
public class QuickSort extends SortExperiment
{
	/**
	 * The name of this algorithm.
	 */
	public static final transient String NAME = "Quick Sort";
	
	protected int m_comparisons = 0;

	public QuickSort(Point p)
	{
		super(NAME, p.getInt(SIZE));
	}

	@Override
	public void sort(int[] array)
	{
		int length = array.length;
		quickSort(array, 0, length - 1);
		writeOutput(COMPARISONS, m_comparisons);
	}

	private void quickSort(int[] array, int lowerIndex, int higherIndex)
	{
		int i = lowerIndex;
		int j = higherIndex;
		// calculate pivot number, I am taking pivot as middle index number
		int pivot = array[lowerIndex+(higherIndex-lowerIndex)/2];
		// Divide into two arrays
		while (i <= j) {
			/**
			 * In each iteration, we will identify a number from left side which 
			 * is greater then the pivot value, and also we will identify a number 
			 * from right side which is less then the pivot value. Once the search 
			 * is done, then we exchange both numbers.
			 */
			while (array[i] < pivot) {
				m_comparisons++;
				i++;
			}
			while (array[j] > pivot) {
				m_comparisons++;
				j--;
			}
			if (i <= j) {
				exchangeNumbers(array, i, j);
				//move index to next position on both sides
				i++;
				j--;
			}
		}
		// call quickSort() method recursively
		if (lowerIndex < j)
			quickSort(array, lowerIndex, j);
		if (i < higherIndex)
			quickSort(array, i, higherIndex);
	}

	private void exchangeNumbers(int[] array, int i, int j) {
		int temp = array[i];
		array[i] = array[j];
		array[j] = temp;
	}

}
